CREATE TABLE ENVELOPE_TYPE
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    TYPE VARCHAR(20),
    TEMPLATE VARCHAR(200),
    PRIMARY KEY (ID)
);

CREATE TABLE CLIENT
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    COMPANY VARCHAR(400) NOT NULL,
    CONTACT_1 VARCHAR(100),
    CONTACT_2 VARCHAR(100),
    CONTACT_3 VARCHAR(100),
    ADDRESS VARCHAR(400),
    TELEPHONE_1 VARCHAR(50),
    TELEPHONE_2 VARCHAR(50),
    ENVELOPE_TYPE_ID BIGINT,
    PRIMARY KEY (ID),
    CONSTRAINT CLIENT_ENVELOPE_TYPE_ID_FK FOREIGN KEY (ENVELOPE_TYPE_ID) REFERENCES ENVELOPE_TYPE (ID)
);

