CREATE SEQUENCE HIBERNATE_SEQUENCE START WITH 1000 INCREMENT BY 1;

CREATE TABLE ENVELOPE_TYPE
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    TYPE VARCHAR(20),
    WIDTH BIGINT,
    HEIGHT BIGINT,
    IMAGE_FILE_NAME VARCHAR(200),
    PRIMARY KEY (ID)
);

CREATE TABLE ENVELOPE_FIELD
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    ENVELOPE_TYPE_ID BIGINT,
    FIELD_NAME VARCHAR(100),
    FIELD_POSITION_X BIGINT,
    FIELD_POSITION_Y BIGINT,
    FIELD_WIDTH BIGINT,
    FIELD_HEIGHT BIGINT,
    DEFAULT_VALUE VARCHAR(100),
    PRIMARY KEY (ID),
    CONSTRAINT ENVELOPE_FIELD_ENVELOPE_TYPE_ID_FK FOREIGN KEY (ENVELOPE_TYPE_ID) REFERENCES ENVELOPE_TYPE (ID)
);

CREATE TABLE CLIENT
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    COMPANY VARCHAR(400) NOT NULL,
    CONTACT_1 VARCHAR(100),
    CONTACT_2 VARCHAR(100),
    CONTACT_3 VARCHAR(100),
    ADDRESS VARCHAR(400) NOT NULL,
    TELEPHONE_1 VARCHAR(50),
    TELEPHONE_2 VARCHAR(50),
    STATUS VARCHAR(20) NOT NULL,
    ENVELOPE_TYPE_ID BIGINT,
    PRIMARY KEY (ID),
    CONSTRAINT CLIENT_ENVELOPE_TYPE_ID_FK FOREIGN KEY (ENVELOPE_TYPE_ID) REFERENCES ENVELOPE_TYPE (ID)
);

CREATE TABLE CLIENT_GROUP
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    GROUP_NAME VARCHAR(200) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE CLIENT_GROUP_MAPPING
(
    CLIENT_GROUP_ID BIGINT,
    CLIENT_ID BIGINT,
    CONSTRAINT CLIENT_GROUP_ID_FK FOREIGN KEY (CLIENT_GROUP_ID) REFERENCES CLIENT_GROUP (ID),
    CONSTRAINT CLIENT_ID_FK FOREIGN KEY (CLIENT_ID) REFERENCES CLIENT (ID)
);